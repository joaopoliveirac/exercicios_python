# 2. Acessar Elementos da Lista
# Dada a lista: nomes = ["Ana", "Bruno", "Carlos", "Daniela", "Eduardo"]
# Peça ao usuário um número de 1 a 5 e exiba o nome correspondente.

nomes = ["Ana", "Bruno", "Carlos", "Daniela", "Eduardo"]
numero = int(input('informe um numero de 1 a 5: '))
print(f'Esse numero equivale ao nome: {nomes[numero]}')

